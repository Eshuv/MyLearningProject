public class Animal {
    boolean isAlive;

    Animal() {
        isAlive = true;
    }

    void eat()
    {
        System.out.println("The animal is eating!");
    }

    void sound()
    {
        System.out.println("Animals makes sound!");
    }

    public boolean isIsAlive() {
        return isAlive;
    }
    
}
